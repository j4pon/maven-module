package org.otros;

public class Cursor {
	private int id;
	private String deltaPL;
	private String deltaJAVA;
	private String beforeJAVA;
	private String fecha;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDeltaPL() {
		return deltaPL;
	}
	public void setDeltaPL(String deltaPL) {
		this.deltaPL = deltaPL;
	}
	public String getDeltaJAVA() {
		return deltaJAVA;
	}
	public void setDeltaJAVA(String deltaJAVA) {
		this.deltaJAVA = deltaJAVA;
	}
	public String getBeforeJAVA() {
		return beforeJAVA;
	}
	public void setBeforeJAVA(String beforeJAVA) {
		this.beforeJAVA = beforeJAVA;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
}
